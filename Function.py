num1 = 5
num2 = 10

def add(num1, num2):
    num = num1 + num2
    return num

def sub(num1, num2):
    num = num1 - num2
    return num

def mul(num1, num2):
    num = num1 * num2
    return num

def div(num1, num2):
    num = num1 / num2
    return num

print(add(10,5))
print(sub(10,15))
print(mul(10,5))
print(div(5,10))


